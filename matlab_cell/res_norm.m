function residuum=res_norm(f,bc)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% norm of residual %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
if nargin==1
   residuum=norm(f);
elseif nargin==2
   [nr,nc]=size(f);
   fdof=[1:nr]';
   pdof=bc(:,1);
   fdof(pdof)=[];
   residuum=norm(f(fdof));
end  
